int		ft_iterative_factorial(int nb)
{
	int s;

	s = nb;
	if (nb < 0)
		return (0);
	if (nb == 0)
		return (1);
	while (s > 1)
	{
		nb = nb * (s - 1);
		s--;
	}
	return (nb);
}
